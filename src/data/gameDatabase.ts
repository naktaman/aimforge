/** ID로 게임 조회 */
export function getGameById(id: string): GameEntry | undefined {
  return GAME_DATABASE.find(g => g.id === id);
}

/** 이름(영문/한국어)으로 검색 */
export function searchGames(query: string): GameEntry[] {
  const q = query.toLowerCase();
  return GAME_DATABASE.filter(
    g => g.name.toLowerCase().includes(q) || g.nameKo.includes(q) || g.id.includes(q)
  );
}

/** 카테고리별 필터 */
export function getGamesByCategory(category: GameCategory): GameEntry[] {
  return GAME_DATABASE.filter(g => g.category === category);
}

/** 엔진별 필터 */
export function getGamesByEngine(engine: GameEngine): GameEntry[] {
  return GAME_DATABASE.filter(g => g.engine === engine);
}

/** GameEntry → Rust-compatible GamePreset 변환용 객체 */
export function toGamePreset(entry: GameEntry) {
  return {
    id: entry.id,
    name: entry.name,
    yaw: entry.yaw,
    default_fov: entry.defaultFov,
    fov_type: entry.fovType,
    default_aspect_ratio: entry.defaultAspectRatio,
    sens_step: entry.sensStep,
    movement_ratio: entry.movementRatio,
  };
}

/** 게임 yaw 맵 (profileWizardStore 호환) */
export function getGameYawMap(): Record<string, number> {
  const map: Record<string, number> = {};
  for (const g of GAME_DATABASE) {
    map[g.name] = g.yaw;
  }
  return map;
}

/** 게임 감도 필드 맵 (profileWizardStore 호환) */
export function getGameSensFieldsMap(): Record<string, GameSensField[]> {
  const map: Record<string, GameSensField[]> = {};
  for (const g of GAME_DATABASE) {
    map[g.name] = g.sensitivityFields;
  }
  return map;
}

