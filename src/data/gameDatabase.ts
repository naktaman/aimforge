/**
 * 게임 감도 변환 데이터베이스
 * 50+ FPS/TPS 게임의 yaw, FOV, 감도 필드 등 메타데이터 관리
 * Rust game_db와 동기화 필요 — 프론트엔드 전용 확장 필드 포함
 */

/** 게임 카테고리 */
export type GameCategory = 'fps' | 'tps' | 'battle-royale' | 'tactical' | 'arena';

/** 게임 엔진 */
export type GameEngine =
  | 'Source'
  | 'Source 2'
  | 'Unreal Engine'
  | 'id Tech'
  | 'IW Engine'
  | 'Unity'
  | 'Frostbite'
  | 'Snowdrop'
  | 'RAGE'
  | 'CryEngine'
  | 'Enfusion'
  | 'Custom';

/** 게임별 감도 입력 필드 정의 */
export interface GameSensField {
  key: string;
  label: string;
  min: number;
  max: number;
  step: number;
  defaultValue: number;
}

/** 게임 데이터 엔트리 — Rust GamePreset의 확장 */
export interface GameEntry {
  /** 고유 ID (Rust GamePreset.id와 동일) */
  id: string;
  /** 영문 이름 */
  name: string;
  /** 한국어 이름 */
  nameKo: string;
  /** 게임 엔진 */
  engine: GameEngine;
  /** yaw 값 (degree per count at sens=1, DPI=1) */
  yaw: number;
  /** FOV 타입 */
  fovType: 'horizontal' | 'vertical' | 'diagonal';
  /** 기본 FOV 값 */
  defaultFov: number;
  /** ADS 지원 여부 */
  hasAds: boolean;
  /** 기본 ADS 배율 (없으면 null) */
  adsMultiplierDefault: number | null;
  /** 감도 입력 필드 정의 */
  sensitivityFields: GameSensField[];
  /** 게임 카테고리 */
  category: GameCategory;
  /** 감도 최소 단위 (null이면 제한 없음) */
  sensStep: number | null;
  /** 이동 시 감도 가중 비율 */
  movementRatio: number;
  /** 기본 화면비 */
  defaultAspectRatio: number;
  /** 데이터 신뢰도: 'verified' = 교차검증, 'estimated' = 엔진 기반 추정 */
  confidence: 'verified' | 'estimated';
}

/** 기본 감도 필드 — 대부분의 게임에서 사용 */
